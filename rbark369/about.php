    <h2>About This Project</h2>
    <p>
This website was created as part of <strong>Assignment #1 for COMP 3512 (Web II: Server Side Development)</strong> at Mount Royal University. 
The goal of this project is to build a dynamic, data-driven PHP website that retrieves information from an <strong>SQLite database</strong> containing fictional users and real stock market data. 
It demonstrates the use of <strong>PHP with PDO</strong> for database connectivity, <strong>SQL INNER JOINs</strong> for combining related data, and clean, semantic HTML and CSS for presentation. 
This assignment also includes a set of <strong>custom API endpoints</strong> returning JSON data, which will be extended in Assignment #2 with JavaScript-based features.
</p>

<p>
Our project showcases an understanding of server-side programming concepts, database relationships, and web design principles. 
We followed best practices for readability and maintainability, using version control through <strong>Git and GitHub</strong> to track progress and collaboration. 
All styling was implemented using custom CSS.
